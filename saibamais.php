            <h3>Doença Cardiovascular</h3>
            <p>Problemas no coração e circulação são comuns entre idosos. Hipertensão, infarto e insuficiência cardíaca podem surgir com o tempo, mas a prática de atividades físicas e alimentação saudável ajudam a prevenir e controlar essas condições.</p>
            <p><strong>Como retardar o avanço:</strong> Manter a pressão arterial sob controle, evitar o sedentarismo e realizar exames regulares de saúde.</p>
        </div>
    </div>
    <div class="info-item">
        <span class="item-number">2.</span>
        <div class="info-content">
            <h3>Artrite e Osteoporose</h3>
            <p>A artrite, especialmente a osteoartrite, e a osteoporose afetam diretamente as articulações e a saúde óssea dos idosos, dificultando movimentos e aumentando o risco de fraturas. O controle dessa doença depende de exercícios adequados e alimentação rica em cálcio.</p>
            <p><strong>Como retardar o avanço:</strong> Fazer fisioterapia, alongamentos, manter uma dieta rica em cálcio e vitamina D, além de evitar quedas.</p>
        </div>
    </div>
    <div class="info-item">
        <span class="item-number">3.</span>
        <div class="info-content">
            <h3>Alzheimer e Demência</h3>
            <p>Doenças cognitivas como Alzheimer afetam a memória e o comportamento, prejudicando a qualidade de vida. O aumento do número de casos está diretamente relacionado ao envelhecimento da população.</p>
            <p><strong>Como retardar o avanço:</strong> Exercícios cognitivos, leitura, interação social, alimentação equilibrada e controle do estresse ajudam a desacelerar o processo.</p>
        </div>
    </div>
